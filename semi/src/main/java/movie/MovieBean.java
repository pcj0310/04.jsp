package movie;

public class MovieBean {
	 	private int movie_id;
	    private String movie_title;
	    private String movie_date;
	    private String movie_dir;
	    private String movie_img;
	    private String movie_info;
	    
		public int getMovie_id() {
			return movie_id;
		}
		public void setMovie_id(int movie_id) {
			this.movie_id = movie_id;
		}
		public String getMovie_title() {
			return movie_title;
		}
		public void setMovie_title(String movie_title) {
			this.movie_title = movie_title;
		}
		public String getMovie_date() {
			return movie_date;
		}
		public void setMovie_date(String movie_date) {
			this.movie_date = movie_date;
		}
		public String getMovie_dir() {
			return movie_dir;
		}
		public void setMovie_dir(String movie_dir) {
			this.movie_dir = movie_dir;
		}
		public String getMovie_img() {
			return movie_img;
		}
		public void setMovie_img(String movie_img) {
			this.movie_img = movie_img;
		}
		public String getMovie_info() {
			return movie_info;
		}
		public void setMovie_info(String movie_info) {
			this.movie_info = movie_info;
		}
}
